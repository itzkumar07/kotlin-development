    val tow_no_Mul = f_no * s_no
    val tow_no_Per = f_no % s_no
   
    println("This is the Arithmetic_Oprators")
    println(tow_no_Add)
    println(tow_no_Sub)
    println(tow_no_Div)
    println(tow_no_Mul)
